
public class setor {
	private String id;
	private String nome_do_setor;
	
	
	
	
	
	
	public setor() {
		
	}
	
	
	public setor(String id, String nome_do_setor) {
		
		this.id = id;
		this.nome_do_setor = nome_do_setor;
	}


	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNome_do_setor() {
		return nome_do_setor;
	}
	public void setNome_do_setor(String nome_do_setor) {
		this.nome_do_setor = nome_do_setor;
	}
	
	
	
	
	

}
