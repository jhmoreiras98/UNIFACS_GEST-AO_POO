
public class escola {
	private String id;
	private String nome;
	
	
	escola(String código,String nome){
		this.id=código;
		this.nome=nome;
		
	}
	
	
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome_da_escola(String nome) {
		this.nome = nome;
	}
	

}
